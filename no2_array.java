import java.util.*;
public class no2_array {
  public static void main(String[] args) {
    //another way to put array
    String [] subjects = {"maths","physics","chemistry","DSA"};
    //to print all index of subject array
    System.out.println(Arrays.toString(subjects));
    //to print any index of array
    System.out.println(subjects[0]);
    System.out.println(subjects[1]);
    System.out.println(subjects[2]);
    System.out.println(subjects[3]);
    //to check the length of array
    System.out.println(subjects.length);
  }  
}
